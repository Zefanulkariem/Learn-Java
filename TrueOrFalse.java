public class TrueOrFalse {
    public static void main(String[] args) {
        int bil1, bil2;

        bil1 = 1;
        bil2 = -1;

        if(bil1 > 0){
            System.out.println(true);
        } else {
            System.out.println(false);
        }

        if(bil2 > 0){
            System.out.println(true);
        } else {
            System.out.println(false);
        }
    }
}
